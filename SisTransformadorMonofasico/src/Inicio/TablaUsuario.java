    public void Eliminar(){
        ImageIcon iiE=new ImageIcon(getClass().getResource("/Icons/deleteaccount.png"));
        int filas=this.jtUsuarios.getSelectedRowCount();
        if(filas==0){
            JOptionPane.showMessageDialog(rootPane, "Debe Seleccionar Fila", "Eliminar", JOptionPane.INFORMATION_MESSAGE);            
        }else{
            int fila=this.jtUsuarios.getSelectedRow();
            String nombre=this.jtUsuarios.getValueAt(fila,0).toString();
            au.Eliminar(nombre);
            modelo.removeRow(fila);
            au.grabarUsuario();
            JOptionPane.showMessageDialog(rootPane, "Usuario Eliminado", "Eliminar", JOptionPane.PLAIN_MESSAGE,iiE);
        }
    }     

